/**
 * Classificazione di un {@link Indizio} in base alla sua natura.
 */
public enum TipoIndizio {
    /** Prova materiale (es. l'arma del delitto, il manoscritto). */
    FISICO,
    /** Dichiarazione raccolta da un personaggio. */
    TESTIMONIALE,
    /** Documento o registro (es. la perizia, il log degli accessi). */
    DOCUMENTALE
}
